function somar(a, b) {
    return a + b;
}
console.log(somar(5, 6)); // 11
//---------------------------------
function subtrair(a, b) {
    return a - b;
}
console.log(subtrair(10, 20)); // -10
//----------------------------------------------
function multiplicar(a, b) {
    return a * b;
}
console.log(multiplicar(50, 100)); // 5000
//----------------------------------------
function dividir(a, b) {
    return a / b;
}
console.log(dividir(100, 500)); // 0.2
